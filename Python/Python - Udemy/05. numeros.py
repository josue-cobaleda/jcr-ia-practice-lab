# Description: Operations with numbers in Python
print(2 + 5)
print(1 - 3)
print(3 * 4) 
print(10 / 2) 

#Exponente. Devuelve el resultado de elevar un número a una potencia
print(4 ** 4) 

#División entera. Devuelve el cociente de una división
print(10 // 3) 

#Módulo. Devuelve el residuo de una división
print(10 % 3) 

#Parentesis. Se ejecuta primero la operación dentro de los parentesis
print((2 + 3) * 4) 

#Precedencia de operadores. Se ejecuta primero la potencia, luego la multiplicación, luego la división y finalmente la suma
print(2 + 3 * 4 ** 2 / 2) 

#Suma 1 al valor de la variable
numero = 20
numero += 1
print(numero) 

#Suma un número entero con un número decimal
print(2+4.9) 

#El operador == en Python compara si dos valores son iguales. Si los valores son idénticos, devuelve True; si son diferentes, devuelve False.
print(5==5) #True
print(5==4) #False

# = (un solo igual) → Se usa para asignar valores a una variable.
# == (doble igual) → Se usa para comparar si dos valores son iguales.
